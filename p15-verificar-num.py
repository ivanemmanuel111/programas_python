# Verificar si un numero entero es positivo, negativo, o cero

print('Verificando si un numero es positivo, negativo o cero ...')

numero =  int(input('Dame un numero ? '))
if numero > 0 :
    print('El numero es positivo') ## Importante dejar el espacio
if numero < 0 :
    print('El numero es negativo')
if numero == 0 : 
    print('El numero es cero')
